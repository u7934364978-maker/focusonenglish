def expand_unit(unit_num):
    filepath = CONTENT_DIR / f'unit{unit_num}.json'
    with open(filepath) as f:
        data = json.load(f)
    
    before = sum(len(b.get('content', [])) for b in data['blocks'])
    
    extra_fn = EXTRA_BLOCKS.get(unit_num)
    if not extra_fn:
        print(f'  Unit {unit_num}: no extra block defined, skipping')
        return
    
    new_block = extra_fn()
    data['blocks'].append(new_block)
    
    after = sum(len(b.get('content', [])) for b in data['blocks'])
    print(f'  Unit {unit_num}: {before} → {after} interactions (+{after - before})')
    
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def main():
    print('=== Expanding Units 11-20 to 60 interactions ===\n')
    for i in range(11, 21):
        expand_unit(i)
    print('\nDone!')

if __name__ == '__main__':
    main()

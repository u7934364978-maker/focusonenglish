def make_mc(uid, block_num, ix_num, prompt_es, options, correct_opt, mastery='vocabulary', complexity=2, concept_tags=None):
    opts = [{'id': f'o{i+1}', 'text': t} for i, t in enumerate(options)]
    return {
        'interaction_id': f'A1_U{uid}_B{uid}_{block_num}_E{ix_num}',
        'type': 'multiple_choice',
        'prompt_es': prompt_es,
        'options': opts,
        'correct_answer': f'o{correct_opt}',
        'mastery_tag': mastery,
        'complexity': complexity,
        'concept_tags': concept_tags or ['vocabulary'],
    }

def make_fill(uid, block_num, ix_num, prompt_es, stimulus_en, correct_answer, mastery='grammar', concept_tags=None):
    return {
        'interaction_id': f'A1_U{uid}_B{uid}_{block_num}_F{ix_num}',
        'type': 'fill_blanks',
        'prompt_es': prompt_es,
        'stimulus_en': stimulus_en,
        'correct_answer': correct_answer,
        'mastery_tag': mastery,
        'complexity': 2,
        'concept_tags': concept_tags or ['grammar'],
    }

def make_match(uid, block_num, ix_num, prompt_es, pairs, correct_answer, mastery='vocabulary'):
    return {
        'interaction_id': f'A1_U{uid}_B{uid}_{block_num}_M{ix_num}',
        'type': 'matching',
        'prompt_es': prompt_es,
        'pairs': pairs,
        'correct_answer': correct_answer,
        'mastery_tag': mastery,
        'complexity': 2,
        'concept_tags': ['vocabulary'],
    }

def make_reorder(uid, block_num, ix_num, prompt_es, words, correct_order, mastery='syntax'):
    opts = [{'id': f'w{i+1}', 'text': w} for i, w in enumerate(words)]
    correct_answer = [f'w{correct_order[i]+1}' for i in range(len(correct_order))]
    return {
        'interaction_id': f'A1_U{uid}_B{uid}_{block_num}_R{ix_num}',
        'type': 'reorder_words',
        'prompt_es': prompt_es,
        'options': opts,
        'correct_answer': correct_answer,
        'mastery_tag': mastery,
        'complexity': 3,
        'concept_tags': ['grammar', 'syntax'],
    }

def make_tf(uid, block_num, ix_num, prompt_es, correct_answer, mastery='grammar'):
    return {
        'interaction_id': f'A1_U{uid}_B{uid}_{block_num}_T{ix_num}',
        'type': 'true_false',
        'prompt_es': prompt_es,
        'options': [{'id': 'o1', 'text': '[[True|Verdadero]]'}, {'id': 'o2', 'text': '[[False|Falso]]'}],
        'correct_answer': 'o1' if correct_answer else 'o2',
        'mastery_tag': mastery,
        'complexity': 2,
        'concept_tags': ['grammar'],
    }

